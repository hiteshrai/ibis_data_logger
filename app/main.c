int main(void)
{
	hardware_init();
	usb_cdc_init();
    
	char * transmit_string = "HELLO HITESH HOW ARE YOU???\r\n";
	uint32_t transmit_string_length = strlen(transmit_string);
	
	while (1)
	{
		usb_cdc_task();
		usb_cdc_write((uint8_t *)transmit_string, transmit_string_length);
	}
}
